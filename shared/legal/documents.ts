// Почему отдельный модуль, а не i18n JSON: юридический текст длинный, версионируется
// целиком и передаётся юристу как единый файл. Держать его строками в i18n рядом с
// UI-подписями неудобно и опасно (легко разъехаться по версиям). Здесь — единый
// источник правды; рендерит его компонент <LegalDocument>. Короткая «обвязка»
// (кнопки, футер, cookie-баннер) остаётся в i18n RU/KK.
//
// ⚠️ Реквизиты продавца берутся из SELLER (плейсхолдеры до регистрации ИП/ТОО).
// ⚠️ Текст — крепкий рабочий черновик под юрисдикцию РК; перед публикацией с приёмом
//    реальной оплаты его должен вычитать казахстанский юрист (ссылки на статьи проверить).

import { LEGAL, SELLER, THIRD_PARTIES } from '~~/shared/config/legal'

export interface LegalSection {
  /** Заголовок раздела */
  heading: string
  /** Абзацы текста */
  body?: string[]
  /** Маркированный список (после body) */
  list?: string[]
  /** Абзацы после списка (когда нужен текст и до, и после list) */
  note?: string[]
}

export interface LegalDoc {
  slug: string
  version: string
  /** Заголовок H1 и <title> */
  title: string
  /** Короткое описание для хаба /legal и meta description */
  summary: string
  /** Вводный абзац под заголовком */
  intro?: string
  sections: LegalSection[]
  /** Показывать блок контактов продавца в конце (email/реквизиты) */
  showContacts?: boolean
}

